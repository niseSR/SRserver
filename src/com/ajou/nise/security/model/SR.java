package com.ajou.nise.security.model;

public class SR {

	private int srNumSeq;
	private String srID;
	private String srName;
	private String srDescription;
	public int getSrNumSeq() {
		return srNumSeq;
	}
	public void setSrNumSeq(int srNumSeq) {
		this.srNumSeq = srNumSeq;
	}
	public String getSrID() {
		return srID;
	}
	public void setSrID(String srID) {
		this.srID = srID;
	}
	public String getSrName() {
		return srName;
	}
	public void setSrName(String srName) {
		this.srName = srName;
	}
	public String getSrDescription() {
		return srDescription;
	}
	public void setSrDescription(String srDescription) {
		this.srDescription = srDescription;
	}

}
	
