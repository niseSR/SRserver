package com.ajou.nise.security.model;

public class CM {

	private int cmNumSeq;
	private String cmID;
	private String cmName;
	private String cmDescription;

	public int getCmNumSeq() {
		return cmNumSeq;
	}
	public void setCmNumSeq(int cmNumSeq) {
		this.cmNumSeq = cmNumSeq;
	}
	public String getCmID() {
		return cmID;
	}
	public void setCmID(String cmID) {
		this.cmID = cmID;
	}
	public String getCmName() {
		return cmName;
	}
	public void setCmName(String cmName) {
		this.cmName = cmName;
	}
	public String getCmDescription() {
		return cmDescription;
	}
	public void setCmDescription(String cmDescription) {
		this.cmDescription = cmDescription;
	}
	
	
}
	
