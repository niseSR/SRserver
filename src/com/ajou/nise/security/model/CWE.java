package com.ajou.nise.security.model;

public class CWE {

	private int cweNumSeq;
	private String cweID;
	private String cweName;
	private String cweDescription;
	private String cweRelatedCAPEC;
	private String cweRelatedCWE;
	
	public int getCweNumSeq() {
		return cweNumSeq;
	}
	public void setCweNumSeq(int cweNumSeq) {
		this.cweNumSeq = cweNumSeq;
	}
	public String getCweID() {
		return cweID;
	}
	public void setCweID(String cweID) {
		this.cweID = cweID;
	}
	public String getCweName() {
		return cweName;
	}
	public void setCweName(String cweName) {
		this.cweName = cweName;
	}
	public String getCweDescription() {
		return cweDescription;
	}
	public void setCweDescription(String cweDescription) {
		this.cweDescription = cweDescription;
	}
	public String getCweRelatedCAPEC() {
		return cweRelatedCAPEC;
	}
	public void setCweRelatedCAPEC(String cweRelatedCAPEC) {
		this.cweRelatedCAPEC = cweRelatedCAPEC;
	}
	public String getCweRelatedCWE() {
		return cweRelatedCWE;
	}
	public void setCweRelatedCWE(String cweRelatedCWE) {
		this.cweRelatedCWE = cweRelatedCWE;
	}

}
	
