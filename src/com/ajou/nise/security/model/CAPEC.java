package com.ajou.nise.security.model;

public class CAPEC {

	private int capecNumSeq;
	private String capecID;
	private String capecName;
	private String capecDescription;
	private String capecPrerequisites;
	
	
	public int getCapecNumSeq() {
		return capecNumSeq;
	}
	public void setCapecNumSeq(int capecNumSeq) {
		this.capecNumSeq = capecNumSeq;
	}
	public String getCapecID() {
		return capecID;
	}
	public void setCapecID(String capecID) {
		this.capecID = capecID;
	}
	public String getCapecName() {
		return capecName;
	}
	public void setCapecName(String capecName) {
		this.capecName = capecName;
	}
	public String getCapecDescription() {
		return capecDescription;
	}
	public void setCapecDescription(String capecDescription) {
		this.capecDescription = capecDescription;
	}
	public String getCapecPrerequisites() {
		return capecPrerequisites;
	}
	public void setCapecPrerequisites(String capecPrerequisites) {
		this.capecPrerequisites = capecPrerequisites;
	}

}
	
