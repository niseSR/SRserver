package com.ajou.nise.security.model;

public class CVE {

	private int cveNumSeq;
	private String cveID;
	private String cveName;
	private String cveDescription;
	
	
	public int getCveNumSeq() {
		return cveNumSeq;
	}
	public void setCveNumSeq(int cveNumSeq) {
		this.cveNumSeq = cveNumSeq;
	}
	public String getCveID() {
		return cveID;
	}
	public void setCveID(String cveID) {
		this.cveID = cveID;
	}
	public String getCveName() {
		return cveName;
	}
	public void setCveName(String cveName) {
		this.cveName = cveName;
	}
	public String getCveDescription() {
		return cveDescription;
	}
	public void setCveDescription(String cveDescription) {
		this.cveDescription = cveDescription;
	}

	
}
	
