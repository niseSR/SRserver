public class Relatedsh {

	private int relatedshNumSeq;
	private String relatedshID;
	private String relatedshUserID;
	private String relatedshName;
	private String relatedshDescription;
	private String relatedshCompany;
	
	
	public int getRelatedshNumSeq() {
		return relatedshNumSeq;
	}
	public void setRelatedshNumSeq(int relatedshNumSeq) {
		this.relatedshNumSeq = relatedshNumSeq;
	}
	public String getRelatedshID() {
		return relatedshID;
	}
	public void setRelatedshID(String relatedshID) {
		this.relatedshID = relatedshID;
	}
	public String getRelatedshUserID() {
		return relatedshUserID;
	}
	public void setRelatedshUserID(String relatedshUserID) {
		this.relatedshUserID = relatedshUserID;
	}
	public String getRelatedshName() {
		return relatedshName;
	}
	public void setRelatedshName(String relatedshName) {
		this.relatedshName = relatedshName;
	}
	public String getRelatedshDescription() {
		return relatedshDescription;
	}
	public void setRelatedshDescription(String relatedshDescription) {
		this.relatedshDescription = relatedshDescription;
	}
	public String getRelatedshCompany() {
		return relatedshCompany;
	}
	public void setRelatedshCompany(String relatedshCompany) {
		this.relatedshCompany = relatedshCompany;
	}
	
	
}
	
